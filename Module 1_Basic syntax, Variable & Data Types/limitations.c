#include <stdio.h>
int main() {
    int a = 1000000000; // declaring an integer variable 'a' and initializing it with the value 10
    long long b = 1000000000000000000; // declaring a long long variable 'b' and initializing it with the value 3.14
  
    printf("The value of a is: %d\n", a); // printing the value of variable 'a'
    printf("The value of b is: %lld\n", b); // printing the value of variable 'b' with 2 decimal places
    return 0;
}